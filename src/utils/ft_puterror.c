#include "../cub3d.h"

int	ft_puterror(char *error)
{
	ft_putendl_fd("\033[38;2;247;59;25;1mERROR", 2);
	ft_putstr_fd(error, 2);
	ft_putendl_fd("\033[0m", 2);
	return (1);
}
